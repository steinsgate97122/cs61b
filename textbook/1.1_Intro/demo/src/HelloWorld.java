// class declaration, in java, all codes lives inside of classes
public class HelloWorld {
    // main method, use {} to denote a section of code
    public static void main(String[] args) {
        System.out.println("Hello world!");  // statements must end with ';'
    }
}

// 使用编译器javac得到HelloWorld.class, 在终端执行: javac HelloWorld.java
// 使用解释器java运行, 在终端执行: java HelloWorld
